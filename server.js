const express = require("express");

const http = require("http");

const { Server } = require("socket.io");

const app = express();

const server = http.createServer(app);

///////////////////////////////////////////////////////////////
// Socket.IO
///////////////////////////////////////////////////////////////

const io = new Server(server,{

    cors:{
        origin:"*"
    }
});

///////////////////////////////////////////////////////////////
// プレイヤー一覧
///////////////////////////////////////////////////////////////

///////////////////////////////////////////////////////////////
// ルーム
///////////////////////////////////////////////////////////////

const rooms = {

    ///////////////////////////////////////////////////////////
    // サンプル
    ///////////////////////////////////////////////////////////

    "kyoto":{

        password:"1234",

        players:{}
    },

    "osaka":{

        password:"abcd",

        players:{}
    }
};

///////////////////////////////////////////////////////////////
// 接続
///////////////////////////////////////////////////////////////

io.on("connection",(socket)=>{

    ///////////////////////////////////////////////////////////
    // ルーム作成
    ///////////////////////////////////////////////////////////

    socket.on(

        "createRoom",

        (data)=>{

            ///////////////////////////////////////////////////
            // 既存確認
            ///////////////////////////////////////////////////

            if(rooms[data.roomId]){

                console.log(
                    "already exists"
                );

                return;
            }

            ///////////////////////////////////////////////////
            // ルーム作成
            ///////////////////////////////////////////////////

            rooms[data.roomId]={

                password:data.password,

                players:{}
            };

            console.log(
                "room created:",
                data.roomId
            );
        }
    );

    ///////////////////////////////////////////////////////////
    // GPS更新
    ///////////////////////////////////////////////////////////

    socket.on("updatePosition",(data)=>{

        ///////////////////////////////////////////////////////
        // room存在確認
        ///////////////////////////////////////////////////////

        const room =
        rooms[data.roomId];

        if(!room){

            console.log(
                "room not found"
            );

            return;
        }

        ///////////////////////////////////////////////////////
        // password確認
        ///////////////////////////////////////////////////////

        if(
            room.password
            !==
            data.password
        ){

            console.log(
                "wrong password"
            );

            return;
        }

        ///////////////////////////////////////////////////////
        // 保存
        ///////////////////////////////////////////////////////

        room.players[data.id]={

            id:data.id,

            name:data.name,

            lat:data.lat,

            lng:data.lng,

            heading:data.heading,

            updatedAt:Date.now()
        };

        ///////////////////////////////////////////////////////
        // 同ルームへ送信
        ///////////////////////////////////////////////////////

        io.emit(

            "players",

            Object.values(
                room.players
            )
        );
    });
});

///////////////////////////////////////////////////////////////
// 5分以上更新ない人削除
///////////////////////////////////////////////////////////////

setInterval(()=>{

    const now=Date.now();

    for(const id in players){

        if(
            now - players[id].updatedAt
            >
            1000*60*5
        ){

            delete players[id];
        }
    }

},30000);

///////////////////////////////////////////////////////////////
// 起動
///////////////////////////////////////////////////////////////

const PORT =
process.env.PORT || 3000;

server.listen(PORT,()=>{

    console.log(
        `server start ${PORT}`
    );
});