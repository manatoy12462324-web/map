const express = require("express");

const http = require("http");

const { Server } = require("socket.io");

const app = express();

const server = http.createServer(app);

///////////////////////////////////////////////////////////////
// Socket.IO
///////////////////////////////////////////////////////////////

const io = new Server(server,{

    cors:{
        origin:"*"
    }
});

///////////////////////////////////////////////////////////////
// プレイヤー一覧
///////////////////////////////////////////////////////////////

///////////////////////////////////////////////////////////////
// ルーム
///////////////////////////////////////////////////////////////

const rooms = {

    ///////////////////////////////////////////////////////////
    // サンプル
    ///////////////////////////////////////////////////////////

    "kyoto":{

        password:"1234",

        players:{}
    },

    "osaka":{

        password:"abcd",

        players:{}
    }
};

///////////////////////////////////////////////////////////////
// 接続
///////////////////////////////////////////////////////////////

io.on("connection",(socket)=>{

    console.log("connected");

    ///////////////////////////////////////////////////////////
    // 位置更新
    ///////////////////////////////////////////////////////////

    socket.on("updatePosition",(data)=>{

    ///////////////////////////////////////////////////////////
    // ルーム存在確認
    ///////////////////////////////////////////////////////////

    const room =
    rooms[data.roomId];

    if(!room){

        console.log(
            "room not found"
        );

        return;
    }

    ///////////////////////////////////////////////////////////
    // パスワード確認
    ///////////////////////////////////////////////////////////

    if(
        room.password
        !==
        data.password
    ){

        console.log(
            "wrong password"
        );

        return;
    }

    ///////////////////////////////////////////////////////////
    // プレイヤー保存
    ///////////////////////////////////////////////////////////

    room.players[data.id]={

        id:data.id,

        name:data.name,

        lat:data.lat,

        lng:data.lng,

        heading:data.heading,

        updatedAt:Date.now()
    };

    ///////////////////////////////////////////////////////////
    // 同じルームだけ送信
    ///////////////////////////////////////////////////////////

    io.emit(

        "players",

        Object.values(
            room.players
        )
    );
});

    ///////////////////////////////////////////////////////////
    // 切断
    ///////////////////////////////////////////////////////

    socket.on("disconnect",()=>{

        console.log("disconnect");
    });
});

///////////////////////////////////////////////////////////////
// 5分以上更新ない人削除
///////////////////////////////////////////////////////////////

setInterval(()=>{

    const now=Date.now();

    for(const id in players){

        if(
            now - players[id].updatedAt
            >
            1000*60*5
        ){

            delete players[id];
        }
    }

},30000);

///////////////////////////////////////////////////////////////
// 起動
///////////////////////////////////////////////////////////////

const PORT =
process.env.PORT || 3000;

server.listen(PORT,()=>{

    console.log(
        `server start ${PORT}`
    );
});